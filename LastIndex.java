package Examination;

public class LastIndex {
	    public static void main(String[] args) {
	    	
	    	        String str = "abcdefghij";
	    	        for (char c = 'a'; c <= 'j'; c++) {
	    	            int lastIndex = str.lastIndexOf(c);
	    	            System.out.print(lastIndex + " ");
	    	        }    	        
	    	    }
	    	}
	    	    
	    	

	    
	



